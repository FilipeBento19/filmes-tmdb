// Função helper para buscar vencedores por categoria
export const getWinnersByCategory = (category) => {
  const winners = []
  
  Object.values(oscarWinners).forEach(year => {
    if (year.categories && year.categories[category]?.winner) {
      winners.push({
        ...year.categories[category].winner,
        oscarYear: year.year,
        ceremony: year.ceremony
      })
    }
  })
  
  return winners
}

// Função para buscar vencedores por ano
export const getWinnersByYear = (year) => {
  return oscarWinners[year] || null
}

// Função para buscar todos os vencedores de Melhor Filme
export const getAllBestPictureWinners = () => {
  return getWinnersByCategory('bestPicture')
}

// Função para buscar vencedores de uma década
export const getWinnersByDecade = (startYear, endYear) => {
  const winners = []
  
  for (let year = startYear; year <= endYear; year++) {
    if (oscarWinners[year]) {
      winners.push({
        year,
        ...oscarWinners[year]
      })
    }
  }
  
  return winners
}

